public class Exerc4 {
    public static void main(String[] args) {

        //4. Números Pares até N
        for (int l = 0; l <= 50; l++) { // While l <= 50, add +1
            if (l % 2 == 0) { // If the value of l % 2 = 0 , is true
                System.out.println("Números Pares: " + l); // Print "pares" numbers
            }
        }
    }
}